function start(descriptor) {
	window.open('http://www.freecast.org/start.php?descriptor='+ descriptor, 'freecast','height=400,width=500');
}

function start(descriptor,lang) {
	window.open('http://www.freecast.org/start.php?descriptor='+ descriptor + '&lang=' + lang, 'freecast','height=400,width=500');
}
